package com.test4.board;

public class Board {
	private int boardNum;
	private String boardName;
	private String boardNick;
	private String boardTime;
	private int boardReply;
	private int boardLook;
	private String boardWrite;
	
	public int getBoardNum() {
		return boardNum;
	}
	public void setBoardNum(int boardNum) {
		this.boardNum = boardNum;
	}
	public String getBoardName() {
		return boardName;
	}
	public void setBoardName(String boardName) {
		this.boardName = boardName;
	}
	public String getBoardNick() {
		return boardNick;
	}
	public void setBoardNick(String boardNick) {
		this.boardNick = boardNick;
	}
	public String getBoardTime() {
		return boardTime;
	}
	public void setBoardTime(String boardTime) {
		this.boardTime = boardTime;
	}
	public int getBoardReply() {
		return boardReply;
	}
	public void setBoardReply(int boardReply) {
		this.boardReply = boardReply;
	}
	public int getBoardLook() {
		return boardLook;
	}
	public void setBoardLook(int boardLook) {
		this.boardLook = boardLook;
	}
	public String getBoardWrite() {
		return boardWrite;
	}
	public void setBoardWrite(String boardWrite) {
		this.boardWrite = boardWrite;
	}
	
	
}
